# Lists
# Lists in Python are mutable

# String List
students = ["a", "b", "c", "d"]
print(students)

# Numbers list
numbers = [ 1, 3, 4, 5, 2, 4, 5]
print(numbers)

# Sorting function for list
numbers.sort()
print(numbers)

# Reverse list
numbers.reverse()
print(numbers)

